package class2;

public class ArithmeticOperatorTest {
    public static void main(String[] args) {
        int number1=20, number2=10, number3;

        number3=number1-number2;
        System.out.println(number3);

        number3=number1+number2;
        System.out.println(number3);

        number3=number1*number2;
        System.out.println(number3);

        number3=number1/number2;
        System.out.println(number3);

        number3=number1*number3;
        System.out.println(number3);

        number3=number1%number2;
        System.out.println(number3);
    }

}
